'use client';

import React, { createContext, useContext, useState, ReactNode } from 'react';

interface CartItem {
    id: number;
    name: string;
    price: number;
    image: string;
    size: string;
    quantity: number;
}

interface CartContextType {
    items: CartItem[];
    addToCart: (item: Omit<CartItem, 'quantity'>) => void;
    removeFromCart: (id: number, size: string) => void;
    updateQuantity: (id: number, size: string, quantity: number) => void;
    updateSize: (id: number, oldSize: string, newSize: string) => void;
    clearCart: () => void;
    totalItems: number;
    totalPrice: number;
}

const CartContext = createContext<CartContextType | undefined>(undefined);

export function CartProvider({ children }: { children: ReactNode }) {
    const [items, setItems] = useState<CartItem[]>([]);

    const addToCart = (item: Omit<CartItem, 'quantity'>) => {
        setItems((prev) => {
            const existing = prev.find((i) => i.id === item.id && i.size === item.size);
            if (existing) {
                return prev.map((i) =>
                    i.id === item.id && i.size === item.size
                        ? { ...i, quantity: i.quantity + 1 }
                        : i
                );
            }
            return [...prev, { ...item, quantity: 1 }];
        });
    };

    const removeFromCart = (id: number, size: string) => {
        setItems((prev) => prev.filter((i) => !(i.id === id && i.size === size)));
    };

    const updateQuantity = (id: number, size: string, quantity: number) => {
        if (quantity <= 0) {
            removeFromCart(id, size);
            return;
        }
        setItems((prev) =>
            prev.map((i) =>
                i.id === id && i.size === size ? { ...i, quantity } : i
            )
        );
    };

    const updateSize = (id: number, oldSize: string, newSize: string) => {
        setItems((prev) => {
            const currentItem = prev.find((i) => i.id === id && i.size === oldSize);
            if (!currentItem) return prev;

            const existingWithNewSize = prev.find((i) => i.id === id && i.size === newSize);

            if (existingWithNewSize) {
                // Merge quantities
                const newQuantity = currentItem.quantity + existingWithNewSize.quantity;
                return prev
                    .filter((i) => !(i.id === id && i.size === oldSize))
                    .map((i) =>
                        i.id === id && i.size === newSize ? { ...i, quantity: newQuantity } : i
                    );
            } else {
                // Just update size
                return prev.map((i) =>
                    i.id === id && i.size === oldSize ? { ...i, size: newSize } : i
                );
            }
        });
    };

    const clearCart = () => setItems([]);

    const totalItems = items.reduce((sum, item) => sum + item.quantity, 0);
    const totalPrice = items.reduce((sum, item) => sum + item.price * item.quantity, 0);

    return (
        <CartContext.Provider
            value={{
                items,
                addToCart,
                removeFromCart,
                updateQuantity,
                updateSize,
                clearCart,
                totalItems,
                totalPrice,
            }}
        >
            {children}
        </CartContext.Provider>
    );
}

export function useCart() {
    const context = useContext(CartContext);
    if (!context) {
        throw new Error('useCart must be used within CartProvider');
    }
    return context;
}
